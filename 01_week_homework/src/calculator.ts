export class Calculator {

    public Add(a: number, b: number): number {
        return a + b;
    }

    public Sub(a: number, b: number): number {
        return a - b;
    }

    public Multiply(a: number, b: number): number {
        return a * b;
    }

    public Divide(a: number, b: number): number {
        return a / b;
    }
    public Sqrt(a: number): number {
        return Math.sqrt(a);
    }

}
